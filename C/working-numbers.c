#include<stdio.h>
#include<stdlib.h>

int main()
{   
    //to print float, always be float
    printf("%f\n", 5 / 4.0);
    //"%d" will print a natural num
    printf("%d\n", 5 / 4);


    int num = 6;
    printf("%d\n", num);


    printf("%f\n", pow(2, 3));


    printf("%f\n", sqrt(36));


    printf("%f\n", ceil(8.5));
    printf("%f\n", floor(8.5));


    return 0;
}